package edifactwsradiopolis.dto;

public class FormatosVo {
	private String formato_id;
	private String formato_clave;
	private String formato_descripcion;
	
	public String getFormato_id() {
		return formato_id;
	}
	public void setFormato_id(String formato_id) {
		this.formato_id = formato_id;
	}
	public String getFormato_clave() {
		return formato_clave;
	}
	public void setFormato_clave(String formato_clave) {
		this.formato_clave = formato_clave;
	}
	public String getFormato_descripcion() {
		return formato_descripcion;
	}
	public void setFormato_descripcion(String formato_descripcion) {
		this.formato_descripcion = formato_descripcion;
	}

}
