package edifactwsradiopolis.dto;

public class AddendaReceptor {
	private String calleD;
	private String noExtD;
	private String noIntD;
	private String coloniaD;
	private String municipioD;
	private String estadoD;
	private String paisD;
	private String cpD;
	private String telefonoD;
	private String correoD;
	
	public String getCalleD() {
		return calleD;
	}
	public void setCalleD(String calleD) {
		this.calleD = calleD;
	}
	public String getNoExtD() {
		return noExtD;
	}
	public void setNoExtD(String noExtD) {
		this.noExtD = noExtD;
	}
	public String getNoIntD() {
		return noIntD;
	}
	public void setNoIntD(String noIntD) {
		this.noIntD = noIntD;
	}
	public String getColoniaD() {
		return coloniaD;
	}
	public void setColoniaD(String coloniaD) {
		this.coloniaD = coloniaD;
	}
	public String getMunicipioD() {
		return municipioD;
	}
	public void setMunicipioD(String municipioD) {
		this.municipioD = municipioD;
	}
	public String getEstadoD() {
		return estadoD;
	}
	public void setEstadoD(String estadoD) {
		this.estadoD = estadoD;
	}
	public String getPaisD() {
		return paisD;
	}
	public void setPaisD(String paisD) {
		this.paisD = paisD;
	}
	public String getCpD() {
		return cpD;
	}
	public void setCpD(String cpD) {
		this.cpD = cpD;
	}
	public String getTelefonoD() {
		return telefonoD;
	}
	public void setTelefonoD(String telefonoD) {
		this.telefonoD = telefonoD;
	}
	public String getCorreoD() {
		return correoD;
	}
	public void setCorreoD(String correoD) {
		this.correoD = correoD;
	}

}
